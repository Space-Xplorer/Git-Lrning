//Arithmetic Operations
let a = 22;
let b = 10;
console.log('Artihmetic Operations')
console.log(a+b)
console.log(a-b)
console.log(a*b)
console.log(a/b)
console.log(a%b)

//Unary Increment and decrement
let c = 3;
console.log('Increment and decrement')
console.log(c)
c++
console.log(c)
c--
console.log(c)

//Comparision operators
let d = 30;
let e = 22;
console.log('Comparision operators')
console.log(d>e)
console.log(e>d)
console.log(e==d)

//Difference between == and ===
let f = true;
let g = 1;
console.log('== ===')
console.log(f==g)
console.log(f===g)
//=== is a strict comparision operator, whereas == tries to convert any value into one datatype to compare.

